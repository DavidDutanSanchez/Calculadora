    private void jRadioMultiplicacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioMultiplicacionActionPerformed
        if (!jTextPrimerNumero.getText().isEmpty() && !jTextSegundoNumero.getText().isEmpty()) {
            double numero1 = Double.parseDouble(jTextPrimerNumero.getText());
            double numero2 = Double.parseDouble(jTextSegundoNumero.getText());
            double resultado = numero1 * numero2;
            jTextRespuesta.setText(String.valueOf(resultado));
        } else {
            JOptionPane.showMessageDialog(null, "Llena todos los Campos");
        }


    }//GEN-LAST:event_jRadioMultiplicacionActionPerformed

    private void jRadioRestaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioRestaActionPerformed
        if (!jTextPrimerNumero.getText().isEmpty() && !jTextSegundoNumero.getText().isEmpty()) {
            double numero1 = Double.parseDouble(jTextPrimerNumero.getText());
            double numero2 = Double.parseDouble(jTextSegundoNumero.getText());
            double resultado = numero1 - numero2;
            jTextRespuesta.setText(String.valueOf(resultado));
        } else {
            JOptionPane.showMessageDialog(null, "Llena todos los Campos");
        }

    }//GEN-LAST:event_jRadioRestaActionPerformed

    private void jRadioSumaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioSumaActionPerformed
        if (!jTextPrimerNumero.getText().isEmpty() && !jTextSegundoNumero.getText().isEmpty()) {
            double numero1 = Double.parseDouble(jTextPrimerNumero.getText());
            double numero2 = Double.parseDouble(jTextSegundoNumero.getText());
            double resultado = numero1 + numero2;
            jTextRespuesta.setText(String.valueOf(resultado));
        } else {
            JOptionPane.showMessageDialog(null, "Llena todos los Campos");

        }
    }//GEN-LAST:event_jRadioSumaActionPerformed

    private void jRadioDivicionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioDivicionActionPerformed
        if (!jTextPrimerNumero.getText().isEmpty() && !jTextSegundoNumero.getText().isEmpty()) {
            if (Double.parseDouble(jTextSegundoNumero.getText()) != 0 && Double.parseDouble(jTextPrimerNumero.getText()) != 0) {

                DecimalFormat df = new DecimalFormat("#.00");
                double numero1 = Double.parseDouble(jTextPrimerNumero.getText());
                double numero2 = Double.parseDouble(jTextSegundoNumero.getText());
                double resultado = numero1 / numero2;
                jTextRespuesta.setText(String.valueOf(df.format(resultado)));

            } else {

                JOptionPane.showMessageDialog(null, "La divicion no se puede dar para 0");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Llena todos los Campos");
        }
    }//GEN-LAST:event_jRadioDivicionActionPerformed

